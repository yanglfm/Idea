package com.day05;

public class Student {

	private int id;
	private String name;
	private int age;
	private String[] lover;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String[] getLover() {
		return lover;
	}
	public void setLover(String[] lover) {
		this.lover = lover;
	}
	
}
